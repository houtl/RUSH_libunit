#include "libft.h"
#include <string.h>

int	atoi_long_test(void)
{
	char	*str;

	str = "9223372036854775807";
	if (ft_atoi(str) != atoi(str))
		return (-1);
	str = "-9223372036854775808";
	if (ft_atoi(str) != atoi(str))
		return (-1);
	return (0);
}
